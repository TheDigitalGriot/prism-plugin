#!/usr/bin/env node
/**
 * port-griotwave.cjs
 *
 * Reads the canonical griotwave token file and regenerates the CSS variable
 * block in `frame-template.html` between `griotwave-tokens-start` and
 * `griotwave-tokens-end` markers.
 *
 * Idempotent: running twice with the same source produces identical output.
 *
 * Usage:
 *   node port-griotwave.cjs
 *   node port-griotwave.cjs --griotwave-path /custom/path/griotwave.tokens.json
 *   node port-griotwave.cjs --check     # exit 1 if frame-template.html would change
 *
 * Prism's signature ember is `neural-blue` (Synthetix) — research, intelligence,
 * deep work. The mapping below preserves the existing CSS variable names so
 * downstream component CSS in frame-template.html keeps working.
 */

const fs = require("fs")
const path = require("path")

const args = process.argv.slice(2)
const checkOnly = args.includes("--check")
const pathFlagIdx = args.indexOf("--griotwave-path")
const griotwavePath =
  pathFlagIdx >= 0 && args[pathFlagIdx + 1]
    ? args[pathFlagIdx + 1]
    : "C:/Users/digit/Developer/griotwave-library/_master/griotwave.tokens.json"

const FRAME_PATH = path.join(__dirname, "frame-template.html")
const START_MARKER = "/* griotwave-tokens-start */"
const END_MARKER = "/* griotwave-tokens-end */"

// ---------------------------------------------------------------------------
// Load griotwave tokens and pick the prism ember
// ---------------------------------------------------------------------------

if (!fs.existsSync(griotwavePath)) {
  console.error(`griotwave tokens not found at: ${griotwavePath}`)
  process.exit(2)
}
const tokens = JSON.parse(fs.readFileSync(griotwavePath, "utf-8"))
const v = (node) => (node && typeof node === "object" && "value" in node ? node.value : node)

const ember = tokens.color.ember["neural-blue"]
const text = tokens.color.text
const glass = tokens.color.glass
const canvas = tokens.color.canvas
const status = tokens.color.status
const beacon = tokens.color.ember.beacon

// ---------------------------------------------------------------------------
// Generate the CSS variable block
// ---------------------------------------------------------------------------

const stamp = `/* griotwave port v${tokens.version} @ ${new Date().toISOString()} — generated by port-griotwave.cjs, do not hand-edit */`

const rootBlock = `    :root {
      /* canvas */
      --bg-primary: ${v(canvas.void)};
      --bg-secondary: ${v(glass["haze-05"])};
      --bg-tertiary: ${v(glass["haze-10"])};
      --border: ${v(glass["rim-08"])};

      /* text */
      --text-primary: ${v(text.voice)};
      --text-secondary: ${v(text.whisper)};
      --text-tertiary: ${v(text.footstep)};

      /* prism ember — neural-blue (Synthetix: research, intelligence, deep work) */
      --accent: ${v(ember.primary)};
      --accent-hover: ${v(ember.hot)};
      --accent-soft: ${v(ember.soft)};
      --accent-wash: ${v(ember.wash)};

      /* status */
      --success: ${v(status.online)};
      --warning: ${v(status.warn)};
      --error: ${v(beacon.primary)};

      /* selection */
      --selected-bg: ${v(ember.soft)};
      --selected-border: ${v(ember.primary)};

      /* griotwave glass recipe */
      --glass-bg: rgba(255,255,255,0.04);
      --glass-border: 1px solid rgba(255,255,255,0.08);
      --glass-radius: 20px;
      --glass-blur: blur(40px) saturate(140%);
      --glass-shadow: 0 1px 0 0 rgba(255,255,255,0.06) inset, 0 1px 0 0 rgba(255,255,255,0.02) inset, 0 20px 60px 0 rgba(0,0,0,0.35);

      /* fidelity primitives — components reference these and the data-fidelity overrides reshape them */
      --fidelity-blur: var(--glass-blur);
      --fidelity-shadow: var(--glass-shadow);
      --fidelity-bloom: 0 0 8px 0 ${v(ember.primary)}, 0 0 20px 0 ${v(ember.soft)};
      --fidelity-border-style: solid;
      --fidelity-saturation: 1;
      --fidelity-opacity: 1;

      /* type */
      --font-voice: ${v(tokens.type.family.voice)};
      --font-code: ${v(tokens.type.family.code)};

      /* easing */
      --ease-tale: ${v(tokens.easing.tale)};
      --ease-song: ${v(tokens.easing.song)};
    }

    /* ===== Fidelity overrides — set data-fidelity="lo|mid|hi" on <body> or fragment root ===== */
    [data-fidelity="lo"] {
      --fidelity-blur: none;
      --fidelity-shadow: none;
      --fidelity-bloom: none;
      --fidelity-border-style: dashed;
      --fidelity-saturation: 0.4;
      --fidelity-opacity: 0.85;
      --accent: ${v(text.whisper)};
      --selected-border: ${v(text.echo)};
      --selected-bg: ${v(glass["haze-02"])};
    }

    [data-fidelity="mid"] {
      --fidelity-blur: blur(8px);
      --fidelity-shadow: 0 1px 0 0 rgba(255,255,255,0.04) inset, 0 8px 24px 0 rgba(0,0,0,0.30);
      --fidelity-bloom: none;
      --fidelity-border-style: solid;
      --fidelity-saturation: 0.85;
    }

    [data-fidelity="hi"] {
      --fidelity-blur: var(--glass-blur);
      --fidelity-shadow: var(--glass-shadow);
      --fidelity-bloom: 0 0 8px 0 ${v(ember.primary)}, 0 0 20px 0 ${v(ember.soft)}, 0 0 60px 0 ${v(ember.wash)};
      --fidelity-border-style: solid;
      --fidelity-saturation: 1.1;
    }`

const newBlock = `${START_MARKER}\n    ${stamp}\n${rootBlock}\n    ${END_MARKER}`

// ---------------------------------------------------------------------------
// Write back into frame-template.html (preserving everything outside markers)
// ---------------------------------------------------------------------------

const frameSrc = fs.readFileSync(FRAME_PATH, "utf-8")
const startIdx = frameSrc.indexOf(START_MARKER)
const endIdx = frameSrc.indexOf(END_MARKER)
if (startIdx < 0 || endIdx < 0 || endIdx < startIdx) {
  console.error(
    `frame-template.html is missing the griotwave-tokens markers. Add:\n  ${START_MARKER}\n  ...existing :root block...\n  ${END_MARKER}`,
  )
  process.exit(2)
}

const before = frameSrc.slice(0, startIdx)
const after = frameSrc.slice(endIdx + END_MARKER.length)
const next = before + newBlock + after

if (checkOnly) {
  // Strip the timestamp from both sides for comparison so --check ignores time drift
  const stripStamp = (s) => s.replace(/@ \d{4}-\d{2}-\d{2}T[^ ]+ /, "@ <ts> ")
  if (stripStamp(frameSrc) === stripStamp(next)) {
    console.log("frame-template.html is in sync with griotwave tokens")
    process.exit(0)
  } else {
    console.error("frame-template.html is OUT OF SYNC with griotwave tokens. Re-run port-griotwave.cjs.")
    process.exit(1)
  }
}

fs.writeFileSync(FRAME_PATH, next)
console.log(`griotwave port v${tokens.version} → ${path.relative(process.cwd(), FRAME_PATH)}`)
