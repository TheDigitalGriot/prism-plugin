#!/usr/bin/env node
/**
 * port-griotwave.cjs
 *
 * Reads the canonical griotwave token file and regenerates the CSS variable
 * block in `frame-template.html` between `griotwave-tokens-start` and
 * `griotwave-tokens-end` markers.
 *
 * Idempotent: running twice with the same source produces identical output.
 *
 * Usage:
 *   node port-griotwave.cjs
 *   node port-griotwave.cjs --griotwave-path /custom/path/griotwave.tokens.json
 *   node port-griotwave.cjs --check     # exit 1 if frame-template.html would change
 *
 * Prism's signature ember is `neural-blue` (Synthetix) — research, intelligence,
 * deep work. The mapping below preserves the existing CSS variable names so
 * downstream component CSS in frame-template.html keeps working.
 */

const fs = require("fs")
const path = require("path")

const args = process.argv.slice(2)
const checkOnly = args.includes("--check")
const pathFlagIdx = args.indexOf("--griotwave-path")
const griotwavePath =
  pathFlagIdx >= 0 && args[pathFlagIdx + 1]
    ? args[pathFlagIdx + 1]
    : "C:/Users/digit/Developer/SkillsForge/griotwave/griotwave-library/_master/griotwave.tokens.json"

const FRAME_PATH = path.join(__dirname, "frame-template.html")
const START_MARKER = "/* griotwave-tokens-start */"
const END_MARKER = "/* griotwave-tokens-end */"

// ---------------------------------------------------------------------------
// Load griotwave tokens and pick the prism embers
// ---------------------------------------------------------------------------

if (!fs.existsSync(griotwavePath)) {
  // Graceful missing-tokens handling: emit a warning and exit 0.
  // This is acceptable on machines without the griotwave-library repo.
  // The --check mode also exits 0 with a skip notice (not a hard failure).
  const msg = `[port-griotwave] griotwave tokens not found at: ${griotwavePath}\n` +
    `  Pass --griotwave-path /your/path/griotwave.tokens.json to specify a custom location.\n` +
    `  Skipping — frame-template.html is unchanged.`
  if (checkOnly) {
    console.log("[port-griotwave] --check skipped: griotwave tokens unavailable")
    process.exit(0)
  }
  console.warn(msg)
  process.exit(0)
}
const tokens = JSON.parse(fs.readFileSync(griotwavePath, "utf-8"))
const v = (node) => (node && typeof node === "object" && "value" in node ? node.value : node)

// All ember families used in frame-template.html
const neural  = tokens.color.ember["neural-blue"]
const bio     = tokens.color.ember.bioluminescent ?? tokens.color.ember.bio
const solar   = tokens.color.ember["solar-flare"]  ?? tokens.color.ember.solar
const voltage = tokens.color.ember.voltage
const beacon  = tokens.color.ember.beacon
const text    = tokens.color.text
const glass   = tokens.color.glass
const canvas  = tokens.color.canvas
const status  = tokens.color.status

// Backward compat alias — existing rootBlock references use `ember`
const ember = neural

// ---------------------------------------------------------------------------
// Generate the CSS variable block
// ---------------------------------------------------------------------------

const stamp = `/* griotwave port v${tokens.version} @ ${new Date().toISOString()} — generated by port-griotwave.cjs, do not hand-edit */`

// Flat native variable names matching frame-template.html's canonical block.
// Tokens not in the griotwave.tokens.json file are marked "derived" and hardcoded.
// Legacy aliases are emitted at the bottom via var() for backward compat.
const rootBlock = `  :root{
    /* canvas */
    --void:${v(canvas.void)}; --ash:${v(canvas.ash)};
    --haze-04:rgba(255,255,255,.04); --haze-05:${v(glass["haze-05"])};
    --haze-06:rgba(255,255,255,.06); --haze-10:${v(glass["haze-10"])};
    --rim-08:${v(glass["rim-08"])}; --rim-10:rgba(255,255,255,.10);
    --rim-15:${v(glass["rim-15"])};

    /* text — semantic scale */
    --voice:${v(text.voice)};
    --echo:${v(text.echo)};
    --whisper:${v(text.whisper)};
    --footstep:${v(text.footstep)};

    /* embers — neural blue (Synthetix: research, intelligence, deep work) */
    --neural:${v(neural.primary)}; --neural-glow:rgba(59,130,246,.22); --neural-wash:${v(neural.soft)};
    /* embers — bioluminescent (success, life, growth) */
    --bio:${v(bio.primary)}; --bio-hot:${v(bio.hot)};
    --bio-glow:rgba(16,185,133,.22); --bio-wash:${v(bio.soft)};
    /* embers — solar flare (warning, energy, attention) */
    --solar:${v(solar.primary)}; --solar-glow:rgba(249,115,22,.22); --solar-wash:${v(solar.soft)};
    /* embers — voltage (selection, confirmation, electric) */
    --voltage:${v(voltage.primary)}; --voltage-glow:rgba(198,249,31,.22); --voltage-wash:${v(voltage.soft)};
    /* embers — beacon (error, danger, stop) */
    --beacon:${v(beacon.primary)};

    /* radii */
    --r-pill:999px; --r-3xl:24px;

    /* glass */
    --blur-frost:blur(40px) saturate(140%);

    /* depth — card and lift variants */
    --depth-card:0 1px 0 0 rgba(255,255,255,.06) inset, 0 20px 60px 0 rgba(0,0,0,.35);
    --depth-lift:0 1px 0 0 rgba(255,255,255,.06) inset, 0 8px 24px 0 rgba(0,0,0,.35);

    /* type */
    --font-voice:${v(tokens.type.family.voice)};
    --font-code:${v(tokens.type.family.code)};

    /* easing */
    --tale:${v(tokens.easing.tale)};
    --pulse-e:${v(tokens.easing.pulse)};

    /* legacy aliases (for backward compat with old content fragments) */
    --bg-primary:var(--void); --bg-secondary:var(--haze-05); --bg-tertiary:var(--haze-10);
    --border:var(--rim-08); --text-primary:var(--voice); --text-secondary:var(--whisper);
    --text-tertiary:var(--footstep); --accent:var(--neural); --accent-hover:${v(neural.hot)};
    --accent-soft:var(--neural-wash); --accent-wash:${v(neural.wash)};
    --success:var(--bio-hot); --warning:var(--solar); --error:var(--beacon);
    --selected-bg:var(--neural-wash); --selected-border:var(--neural);
  }

  /* ===== FIDELITY OVERRIDES ===== */
  [data-fidelity="lo"]{
    --depth-card:none; --depth-lift:none; --blur-frost:none;
    --neural:rgba(255,255,255,.60); --bio:rgba(255,255,255,.50);
    --solar:rgba(255,255,255,.50); --voltage:rgba(255,255,255,.50);
  }
  [data-fidelity="lo"] .amb{display:none}
  [data-fidelity="lo"] body::before{opacity:.4}
  [data-fidelity="lo"] .opt,.card{border-style:dashed}

  [data-fidelity="mid"]{
    --blur-frost:blur(8px);
    --depth-card:0 1px 0 0 rgba(255,255,255,.04) inset, 0 8px 24px 0 rgba(0,0,0,.30);
    --depth-lift:0 1px 0 0 rgba(255,255,255,.03) inset, 0 4px 12px 0 rgba(0,0,0,.25);
  }
  [data-fidelity="mid"] .amb{opacity:.25}

  /* hi is the default :root values — full griotwave glass */`

const newBlock = `${START_MARKER}\n    ${stamp}\n${rootBlock}\n    ${END_MARKER}`

// ---------------------------------------------------------------------------
// Write back into frame-template.html (preserving everything outside markers)
// ---------------------------------------------------------------------------

const frameSrc = fs.readFileSync(FRAME_PATH, "utf-8")
const startIdx = frameSrc.indexOf(START_MARKER)
const endIdx = frameSrc.indexOf(END_MARKER)
if (startIdx < 0 || endIdx < 0 || endIdx < startIdx) {
  console.error(
    `frame-template.html is missing the griotwave-tokens markers. Add:\n  ${START_MARKER}\n  ...existing :root block...\n  ${END_MARKER}`,
  )
  process.exit(2)
}

const before = frameSrc.slice(0, startIdx)
const after = frameSrc.slice(endIdx + END_MARKER.length)
const next = before + newBlock + after

if (checkOnly) {
  // Strip the timestamp from both sides for comparison so --check ignores time drift
  const stripStamp = (s) => s.replace(/@ \d{4}-\d{2}-\d{2}T[^ ]+ /, "@ <ts> ")
  if (stripStamp(frameSrc) === stripStamp(next)) {
    console.log("frame-template.html is in sync with griotwave tokens")
    process.exit(0)
  } else {
    console.error("frame-template.html is OUT OF SYNC with griotwave tokens. Re-run port-griotwave.cjs.")
    process.exit(1)
  }
}

fs.writeFileSync(FRAME_PATH, next)
console.log(`griotwave port v${tokens.version} → ${path.relative(process.cwd(), FRAME_PATH)}`)
